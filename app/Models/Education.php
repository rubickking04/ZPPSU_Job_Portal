{
    use HasFactory;

    /**
     * The attributes that are mass assignable.
     *
     * @var array<int, string>
     */
    protected $fillable = [
        'user_id',
        'level_of_education',
        'field_of_study',
        'school_name',
        'start_date',
        'end_date',
    ];
}
